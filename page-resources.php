                a break during the move. ​
                <br>
                <br>
                It is also a very good idea to have a ” bits box ” keep TV remotes , bed screws and bolts ,
                a few tools , some cleaning materials , Packing tape and a knife and anything else that you
                may need to retain and use at a moments notice. ​
