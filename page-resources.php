            <p>
                Book your removal dates at least 2 weeks before your removal date — do this in writing !
                accept the quotation and get written confirmation that your removal team has been booked.​​
                Moving home is a high risk time and it is strongly recommended that you insure all goods to
                be moved or stored. – Do this through your own insurer or ask your Removals consultant to
                assist you in this regard.​​ Do clear out your home and dispose of any unwanted items – dump /
                sell or donate unwanted
                items – there are many welfare organisations that will gladly accept donated items.​​
                <br>
                <br />
                <span class="boldText">5 Days to go</span>
                Double check the following ** That your move dates are booked ** That you
                have signed and submitted your acceptance forms. Be comfortable that you have insured your
                goods during transit. If you are attending to your own packing you need to be well on your
                way to having packed everything.​​
                <br>
                <br>
                <span class="boldText">3 Days to go</span>
                Advise your mover what Items still need to be packed – Most reputable removal
                companies are easily able to assist you with packing of smalls / breakables / glass tops /
                TV sets and fragiles like pictures and lamps.​​
                <br>
                <br>
                <span class="boldText">Pets / Cars</span>
                Make sure that these are moved and out of the way on move days – animals are
                especially at risk of being traumatised and they need to be safely kenneled or out of the
                way.​​
                <br>
                <br>
                Keys / Cell phones / Plane tickets / Firearms / Wallets / Valuables and travel bags / Goods
                not to be moved – Make sure that these are kept safe and are not left lying around – It is a
                good Idea to designate a room and to keep these items separate and safely locked up.​
                <br>
                <br>
                Make sure that you have paid for your removal or ensure that you have internet access to
                make a transaction. ​
                <br>
                <br>
